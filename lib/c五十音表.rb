# -*- coding: utf-8 -*-
class C五十音表
  def initialize
    @五十音表 = {
      あ行: 'あいうえお',
      か行: 'かきくけこ',
      さ行: 'さしすせそ',
      た行: 'たちつてと',
      な行: 'なにぬねの',
      は行: 'はひふへほ',
      ま行: 'まみむめも',
      や行: 'や　ゆ　よ',
      ら行: 'らりるれろ',
      わ行: 'わゐ　ゑを'
    }

    # が行(), さ行() ... 操作を追加
    @五十音表.each_key do |行|
      eval <<-RUBY
            def #{行}
              @五十音表[:#{行}]
            end
          RUBY
    end
  end

  def 五十音表
    @五十音表
  end

  def 合成(かな)
    nil
  end
end

